  public result(Process terminate[],int count1,int tme)
  {
       initComponents();
       DefaultTableModel dtm=(DefaultTableModel)jTable1.getModel();
       String colm[]={"","","",""};
       int avwt=0,avtt=0;
       double th;
       for(int k=0;k<count1;k++)
   {
      
       if(terminate[k]!=null)
       {
           colm[0]=terminate[k].id;
           colm[1]=terminate[k].name;
           colm[2]=terminate[k].wt+"";
           int tt=terminate[k].wt+terminate[k].burst_time;
           colm[3]=tt+"";
           avwt=terminate[k].wt+avwt;
           avtt=tt+avtt;
           
             dtm.addRow(colm);
       }
   }
       avwt=avwt/count1;
       avtt=avtt/count1;
       th=(count1/(tme+1));
       th=2.5;
       jLabel5.setText(avwt+"");
       jLabel4.setText(avtt+"");
       jLabel6.setText(th+"");
       jLabel7.setText(count1+"");
       jLabel8.setText(tme+"");
       
  }
  
